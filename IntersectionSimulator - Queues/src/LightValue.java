/**
 * The LightValue enum lists the phases that a stoplight can be in, which are
 * GREEN, RED, LEFT_SIGNAL. There are rules to each of the lights. GREEN 
 * indicates that vehicles in right and middle lanes can proceed. LEFT_SIGNAL
 * indicates that vehicles only in the left lane can. RED indicates that no 
 * lane may proceed.
 * 
 * @author Bryan Wong
 * 		SBUID#115226034
 * 		Recitation#04
 */
public enum LightValue {
	GREEN, RED, LEFT_SIGNAL
}
